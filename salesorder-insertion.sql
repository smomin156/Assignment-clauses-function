CREATE TABLE CLIENT_MASTER
(
 CLIENT_NO VARCHAR(6) PRIMARY KEY
CONSTRAINT CLIENT_NO CHECK(CLIENT_NO LIKE'C%'),
NAME VARCHAR(20) NOT NULL,
CITY VARCHAR(15),
STATE VARCHAR(15),
PINCODE NUMERIC(6),
BAL_DUE NUMERIC(10,2)
);

CREATE TABLE PRODUCT_MASTER
(
PRODUCT_NO VARCHAR(6) PRIMARY KEY
CONSTRAINT PRODUCT_NO CHECK(PRODUCT_NO LIKE'P%'),
DESCRIPTION VARCHAR(50) NOT NULL,
PROFIT_PERCENT NUMERIC(3,2) NOT NULL,
UNIT_MEASURE VARCHAR(10) NOT NULL,
QTY_ON_HAND NUMERIC(8) NULL,
RECORDER_LVL NUMERIC(8) NOT NULL,
SELL_PRICE NUMERIC(8,2) NOT NULL
CONSTRAINT PRODUCT_SELL_PRICE CHECK(SELL_PRICE>0),
COST_PRICE NUMERIC(8,2) NOT NULL
CONSTRAINT PRODUCT_COST_PRICE CHECK(COST_PRICE>0)
);

CREATE TABLE SALESMAN_MASTER
(
SALESMAN_NO VARCHAR(6) PRIMARY KEY
CONSTRAINT SALESMAN_NO CHECK(SALESMAN_NO LIKE'S%'),
SALESMAN_NAME VARCHAR(20) NOT NULL,
ADDRESS1 VARCHAR(30) NOT NULL,
ADDRESS2 VARCHAR(30),
CITY VARCHAR(20),
PINCODE VARCHAR(6),
STATE VARCHAR(20),
SAL_AMT NUMERIC(8,2) NOT NULL
CONSTRAINT SALESMAN_SALE_AMOUNT CHECK(SAL_AMT>0),
TGT_TO_GET NUMERIC(8,2) NOT NULL
CONSTRAINT SALESMAN_TGT_TO_GET CHECK(TGT_TO_GET>0),
YTD_SALES NUMERIC(6,2) NOT NULL,
REMARKS VARCHAR(60)
);

CREATE TABLE SALES_ORDER
(
S_ORDER_NO VARCHAR(6) PRIMARY KEY
CONSTRAINT S_ORDER_NO CHECK(S_ORDER_NO LIKE'O%'),
S_ORDER_DATE DATE,
CLIENT_NO VARCHAR(6)
CONSTRAINT S_ORDER_CLIENT_NO REFERENCES CLIENT_MASTER(CLIENT_NO),
DELY_TYPE CHAR(1) DEFAULT('F')
CONSTRAINT S_ORDER_DELY_TYPE CHECK(DELY_TYPE IN('P','F')),
BILLYN CHAR(1)DEFAULT('N')
CONSTRAINT S_BILLYN_TYPE CHECK(BILLYN IN('Y','N')),
SALESMAN_NO VARCHAR(6)
CONSTRAINT S_ORDER_SALESMAN_NO REFERENCES
SALESMAN_MASTER(SALESMAN_NO),
DELY_DATE DATE,
ORDER_STATUS VARCHAR(10),
CONSTRAINT S_ORDER_ORDER_STATUS CHECK(ORDER_STATUS IN('IN PROCESS','FILFILLED','BACKORDER','CANCELED'))
);

CREATE TABLE SALES_ORDER_DETAILS
(
S_ORDER_NO VARCHAR(6) 
CONSTRAINT SALES_ORDER_DETAILS_S_ORDER_NO REFERENCES
SALES_ORDER(S_ORDER_NO),
PRODUCT_NO VARCHAR(6)
CONSTRAINT SALES_ORDER_DETAILS_PRODUCT_NO REFERENCES
PRODUCT_MASTER(PRODUCT_NO),
QTY_ORDERED NUMERIC(8),
QTY_DISP NUMERIC(8),
PRODUCT_RATE NUMERIC(10,2)
);

CREATE TABLE CHALLAN_HEADER
(
CHALLAN_NO VARCHAR(6) PRIMARY KEY
CONSTRAINT CHALLAN_NO CHECK(CHALLAN_NO LIKE'CH%'),
S_ORDER_NO VARCHAR(6)
CONSTRAINT CHALLAN_HEADER_S_ORDER_NO REFERENCES
SALES_ORDER(S_ORDER_NO),
CHALLAN_DATE DATE NOT NULL,
BILLED_YN CHAR(1) DEFAULT('N')
CONSTRAINT CHALLAN_HEADER_BILLED_DATE CHECK(BILLED_YN  IN('Y','N'))
);

CREATE TABLE CHALLAN_DETAILS
(
CHALLAN_NO VARCHAR(6) 
CONSTRAINT CHALLAN_NO_CHALLAN_HEADER REFERENCES CHALLAN_HEADER(CHALLAN_NO),
PRODUCT_NO VARCHAR(6) 
CONSTRAINT pRODUCT_NO_PRODUCT_MASTER REFERENCES PRODUCT_MASTER(PRODUCT_NO),
QTY_DISP NUMERIC(4,2) NOT NULL,
CONSTRAINT PRIMARY_KEY_CHALLAN_DETAILS PRIMARY KEY(CHALLAN_NO ,PRODUCT_NO)
);


